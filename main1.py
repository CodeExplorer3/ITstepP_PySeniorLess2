# Класс УчебныйПредмет
class УчебныйПредмет:
    название: str
    часы: int
    включен: bool

    def __init__(self, название: str, часы: int, включен: bool):
        self.название = название
        self.часы = часы
        self.включен = включен

    def информация_о_предмете(self):
        print(f'Предмет: {self.название} | Часы: {self.часы} | Включен: {self.включен}')

# Класс Студент
class Студент:
    имя: str
    фамилия: str
    предметы: list

    def __init__(self, имя: str, фамилия: str, предметы: list):
        self.имя = имя
        self.фамилия = фамилия
        self.предметы = предметы

    def информация_о_студенте(self):
        print(f'Студент: {self.имя} | {self.фамилия}')

    def информация_все(self):
        self.информация_о_студенте()
        for предмет in self.предметы:
            предмет.информация_о_предмете()

# Класс Группа
class Группа:
    название: str
    возрастная_категория: str
    студенты: list

    def __init__(self, название: str, возрастная_категория: str, студенты: list):
        self.название = название
        self.возрастная_категория = возрастная_категория
        self.студенты = студенты

    def информация_о_группе(self):
        print(f'Группа: {self.название} | Возрастная категория: {self.возрастная_категория} | Количество студентов: {len(self.студенты)}')
        for студент in self.студенты:
            студент.информация_все()

# Функция для создания предметов с клавиатуры
def создать_учебные_предметы():
    предметы = []
    кол_во_предметов = int(input("Введите количество предметов: "))
    for _ in range(кол_во_предметов):
        название = input("Введите название предмета: ")
        часы = int(input("Введите количество часов: "))
        включен = input("Предмет включен (True/False): ") == "True"
        предметы.append(УчебныйПредмет(название, часы, включен))
    return предметы

# Функция для создания студентов с клавиатуры
def создать_студентов():
    студенты = []
    кол_во_студентов = int(input("Введите количество студентов: "))
    for _ in range(кол_во_студентов):
        имя = input("Введите имя студента: ")
        фамилия = input("Введите фамилию студента: ")
        предметы = создать_учебные_предметы()
        студенты.append(Студент(имя, фамилия, предметы))
    return студенты

# Пример использования
if __name__ == "__main__":
    название_группы = input("Введите название группы: ")
    возрастная_категория = input("Введите возрастную категорию: ")
    студенты = создать_студентов()
    группа = Группа(название_группы, возрастная_категория, студенты)
    группа.информация_о_группе()
